#include "dog_api_cpp_.h"


////////////////////////////////////////////////////////////////////
// Construction/Destruction
////////////////////////////////////////////////////////////////////

CDogInfo::CDogInfo()
    : m_pszInfo(NULL)
{
}

CDogInfo::~CDogInfo()
{
    clear();
}

////////////////////////////////////////////////////////////////////
// Implementation
////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////
CDogInfo::operator const char *() const
{
    return m_pszInfo;
}

////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////
void CDogInfo::clear()
{
    if (NULL != m_pszInfo)
    {
        dog_free(m_pszInfo);
        m_pszInfo = NULL;
    }
}

////////////////////////////////////////////////////////////////////
//
////////////////////////////////////////////////////////////////////
const char* CDogInfo::getInfo() const
{
    return m_pszInfo;
}

